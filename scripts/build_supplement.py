INSTR_W2Y = "请将以下白话文翻译为文言文："
INSTR_Y2W = "请将以下文言文翻译为白话文："

def to_jsonl_record(direction: str, src: str, tgt: str) -> dict:
    instr = INSTR_W2Y if direction == "w2y" else INSTR_Y2W
    return {
        "messages": [
            {"role": "system", "content": SYSTEM},
            {"role": "user", "content": f"{instr}{src}"},
            {"role": "assistant", "content": tgt},
        ]
    }

def main():
    out_path = Path(__file__).resolve().parent.parent / "data" / "supplement.jsonl"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as f:
        for d, s, t in PAIRS:
            f.write(json.dumps(to_jsonl_record(d, s, t), ensure_ascii=False) + "\n")
    print(f"wrote {len(PAIRS)} samples -> {out_path}")

if __name__ == "__main__":
    main()
